#include <stdio.h>
#include <math.h>

int main(int argc, char const *argv[])
{
	printf("A1(11111*11111)=%d\n", 11111*11111);
	printf("A1(111111*111111)=%d\n", 111111*111111);
	printf("A1(111111111*111111111)=%d\n", 111111111*111111111);

	printf("A2(11111.0*11111.0)=%f\n", 11111.0*11111.0);
	printf("A2(111111.0*111111.0)=%f\n", 111111.0*111111.0);
	printf("A2(111111111.0*111111111.0)=%f\n", 111111111.0*111111111.0);

	printf("A3(sqrt(-10))=%d\n", sqrt(-10));
	printf("A3(sqrt(-10))=%f\n", sqrt(-10));

	printf("A4(1.0/0.0)=%d\n", 1.0/0.0);
	printf("A4(1.0/0.0)=%f\n", 1.0/0.0);

	printf("A4(0.0/0.0)=%d\n", 0.0/0.0);
	printf("A4(0.0/0.0)=%f\n", 0.0/0.0);

	printf("A5(1/0)=%d\n", 1/0);
	printf("A5(1/0)=%f\n", 1/0);
	return 0;
}